/*
Las fases del mercado.
*/

/*

Ciclo economico en crecimiento:
--> Se tienen intereses bajos
--> Crece la inversión
--> Crece el empleo
--> Crece el consumo
--> Crecen los beneficios

Ciclo economico en decrecimiento (Presentes en 7 años de crecimiento):
--> Decienden los beneficios
--> Existen cierres de empresas
--> Aumento de desempleo
--> Baja el consumo y los precios
--> Estado social de desesperacion y depresión colectiva

Tras una reseción exoste un deflación.

Las fases emocionales del mercado son:

Depresión -> Esperanza -> Alivio -> Optimismo -> Entusiasmo -> Euforia -> Ansiedad
-> Negación -> Miedo -> desesperacion -> Pánico -> Desaliento -> Depresión

Participantes del mercado:

--> Manos fuertes: Son la banca institucional, Grandes fondos, fondos de inversión,
                  Sindicatos de inversión, un solo inversor con mucho dinero.

--> Manos debiles: Particulares y analistas técnicos.

--> Empresas de herramienta de cobertura

Fase de acumulación.
--> El precio no es atractivo para los compradores privados
--> Existe un pesimismo por parte de los tenedores de acciones.
--> Las "Manos fuertes" comienzan el proceso de compra haciendolo por tramos para estabilizar la caida.
--> Los grandes inversores mantienen en secreto sus estrategias de compra

Existen patrones de acumulación:
--> "W" 1ra variante, cuenta con un doble suelo técnico.
--> "W" 2da variante, Tiene el segundo minimo por debajo del anterior.
--> "W" 3ra variante, El segundo minimo se encuentra por arriba del primero.
--> H-C-H (Hombro-Cabeza-Hombro) Invertido, compuesto por una "W" de 2da Variante y una de 3ra variante,
    Maximos a la misma altura.

Fase Expansiva.
El precio sale de la zona de acumulación, el proceso de compras ha terminado, en las fases de acumulación es
necesario no realizar operaciones para evitar perdidas, esta fase es la más segura  y la de mayor duración.

Fase Particulares.
En esta fase es donde compran las masas, que no tienen conocimiento de los mercados, todo basado de las
noticias macro, las probabilidades de subida son bajas ya que se encuentra en una fase ya madura.

Fase de distribución.
El mercado continua muy activo, genera euforia y expectativas llamando la atención de las "Manos debiles"
Las "Manos Fuertes" empiezan las ventas.

Patrones de distribución:
--> "M" 1ra Variante, doble techo técnico.
--> "M" 2da variante, el segundo máximo se encuentra superior al primero.
--> "M" 3ra variante, el segundo máximo es menor que el primero.
--> H-C-C (Hombro-Cabeza-Hombro), compuesto por "M" de segunda variante y una de tercera variante.

Fase de pánico.
Comienza la caida, existe la deseperación y pánico entre los inversores y la sociedad, la caida puede ser mayor
debido a las cuentas que no cuentan con  capital o por un stop, cuando la ciad  es muy abrupta se inicia nuevamente
el proceso de compra.

Simplificación de los mercados.
-- Sea codicioso cuando todo el mundo sea temeroso.
-- Sea temeroso cuando todo el mundo sea codicioso.

*/
