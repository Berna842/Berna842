/*

 Enterprise Resource Planning (ERP) es un software que integra todos los elementos de gestion de
 la empresa en una misma plataforma.

 Existen diferentes tipos de ERP:
    -- Según sus necesidades de gestión.
      -- ERP Horizontal:
          Cubre los procesos de gestión habituales de cualquier empresa.
      -- ERP Vertical:
          Diseñado para un sector en especifico.
    -- Según la tecnología del proveedor.
      -- ERP Estandar:
          Trae funcionalidades definidas y rigidas.
      -- ERP Personalizable:
          Proporciona una adaptación del sistema a los procesos.
  Ventajas:
    ERP Horizontal
      – Cubre procesos comunes a todas las empresas.
      – Rápida puesta en marcha.
      – Uso más sencillo y menor necesidad de información.
      – Posibilidad de personalización para cubrir necesidades concretas.
      – Ahorro en costes.
    ERP Vertical
      – Adaptado a las necesidades de un sector o nicho en concreto.
      – Sistema probado durante décadas.
      – Recoge las mejores prácticas de cada sector.
    ERP Estándar
      – Válidos para empresas sin particularidades en su gestión.
      – Gran capacidad de innovación y desarrollo a futuro.
      – Innovación constante y ahorro en costes.
    ERP Personalizado
      – Recoge las necesidades concretas de gestión de tu empresa.
      – Se alcanza máxima eficiencia operativa.
      – Permite la mejora continua, siempre y cuando el sistema sea fácilmente adaptable.

  Desventajas:
    ERP Horizontal
      – Riesgo de sobrecoste por posibles desarrollos a medida.
      – Dificultad en reflejar procesos específicos, si no es configurable.
    ERP Vertical
      – En ocasiones, puede suponer un mayor coste.
      – Mantenimiento constante.
    ERP Estándar
      – Necesidad de adaptación a la forma de trabajar de una tecnología global.
      – Dada la rigidez de estos sistemas, se pierde flexibilidad y agilidad en la gestión empresarial.
    ERP Personalizado
      – Si la plataforma no es flexible suele implicar una mayor inversión y lentitud ante adaptaciones.
      – Con proveedores locales y pequeños no está garantizada la sostenibilidad e innovación
        tecnológica en el tiempo.

*/
