/*Lista de modulos para un core bancario*/

/*
Un core bancario es un sistema el cual procesa transacciones bancarias y publica las actualizaciones de cuentas
y otros registros financieros.
Tiene las capacidades de procesamiento de depósitos, préstamos, creditos e interfaces para aplicaciones de contabilidad
y herramientas de informes.
Debe de proporcionar toda la seguridad, protección y solidez sobre la cual los bancos han construido su reputación.

Algunas funcionalidades adicionales de los core bancarios son, el calculo de los ntereses, la administración de las
cuents nuevas y la gestión de actividades de relación al cliente.

Deben de cumplirse las siguientes caracteristicas:

--> Mantener una implementación basada en estándares abiertos para facilitar la integración con otras herramientas
    técnologías.
--> Invertir en la nube privada, plataformas rápidas y con funciones de seguridad avanzadas.
--> Contar con una solución escalable horizontaly verticalmente que admita nuevos datos y clientes sin generar problemas.

Las caracteristicas básicas son:

--> Enfoque en el cliente.
--> Gestión de depósitos y retiros.
--> Gestión de transacciones.
--> Procesamiento de pagos: efectivo, cheques y otros
--> Dedicación a la gestión de relaciones con clientes.
--> Proyección de nuevos productos bancarios.
--> Disponibilidad y gestión de prestamos.
--> Gestión de cuentas.
--> Establecimiento de criterios para saldos mínimos, tasas de interés, número de retiros permitidos y emisiones de este tipo.

Algunos modulos de los core bancarios son:

--> Modulos del core:
    --> Cuentas de ahorro
    --> Caja
    --> Cambios
    --> Depósitos a plazos
    --> Inversiones
    --> Obligaciones Financieras
    --> Crédito / Cartera
    --> Cobranzas
    --> Garantías
    --> Cuentas corrientes.
    --> Tarjeta de débito.
    --> Banca Virtual.
--> Relación con el cliente:
    --> Información del cliente.
    --> Referencias.
    --> Lealta del cliente
    --> Posicion financiera.
--> Gestión administrativa:
    --> Portal de branch
    --> Contabilidad General.
    --> Presupuesto.
    --> Compras y proveedores.
    --> Cuentas por pagar.
    --> Bancos.
    --> Activos Fijos.
    --> Facturación.
    --> Cuentas por cobrar.
    --> Nomina.
    --> Manejo de impuestos.
--> Automatización de procesos y servicios:
    --> Productos
    --> Gestión de documentos
    --> Procesos por Workflow.
    --> Validación / Scoring.
--> Administración Integral de riesgos:
    --> Riesgo Operativo.
    --> Riesgo de liquidez.
    --> Riesgo de credito.
    --> Prevención de lavado de dinero.
--> Inteligencía de negocios:
    --> Metas / Seguimiento.
    --> Reportes Regulatorios.
    --> Analisis de negocios.
    --> Indicadores financieros.

*/
