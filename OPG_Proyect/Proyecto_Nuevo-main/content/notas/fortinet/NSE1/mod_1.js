/*

Existen diferentes tipos de actores que tratan de vulnerar o realizar ataques
a sistemas o personas atravez de internet, los cuales se dividen en los siguientes:

*/

/*
Explorador:
Su principal herramienta es el phishing, lo cual es utilizado para obtener el usuario y la
contraseña de las personas, el phishing funciona utilizando un servidor web que donde se monta
una pagina de login, identica a la pagina a la que se desea tener los datos de acceso.
Una vez hecho esto, y confirmado que todo se muestra de forma correcta, se realiza un envio
de correo electronico suplantando a la verdadera pagina, este correo incluira un enlace de
ayuda el cual redigira al sistema malicioso.
*/

/*
Hacktivista:
Estos realizan ataques a gobiernos o empresas a las que el conjunto de personas no estan de
acuerdo, estos realizan paticiones de bugeo a los sistemas web atacados.
La principal herramienta de un Hacktivista es el DDoS (Distribuited Denail of Service),
un ataque distribuido de denegación de servicio, este es realizado por medio de una botnet,
la botnet es creada por medio de malware que se instala de manera indeseada, por medio de
contenido adjunto a correos electronicos.
*/

/*
Terroristas Ciberneticos:
Realizan ataques malintencionados a empresas o personas de alta importancia para ellos
tambien hacen uso de DDoS para invertervenir o interrumpir la conexión a internet, infiltrandose
a sistemas, para obtener información sensible o bien para exponer información personal de los
objetivos, o bien corrompiendo información de las empresas.
Para poder realizar su cometido hacen uso de Spear phishing, que basicamente es realizar
ataques de phishing dirigidos a personas de interes.
*/

/*
Criminales Ciberneticos:
Realizan ataques Ciberneticos a terminales bancarias, utilizan malwares para poder ingresar a
una red y así poder robar los datos bancarios de las tarjetas de credito, así mismo utilizan
el ransomware, para obtener directamente la información bancaria de computadoras que estan
infectadas, encriptando la información del disco duro y exigiendo un pago en criptomonedas
para poder brindarles la llave de encriptación para recuperar su información.
*/

/*
Guerreros Ciberneticos:
Utilizan el chantaje, espionaje para poder acceder o destruir infrestructura critica, normalmente
trabajando para el gobierno o para los militares de sus paises, utilizando explotaciones
conocidas e incluso desarrollando las propias, ya que cuentan con los recursos necesarios.
Su principal herramienta es el uso de vulnerabilidades que existen en los sitemas operativos,
tambien conocidos como explotaciones de Día Cero(Zero Day), explotaciones cuya solucion no ha
sido creada todavía, y para cuando exista un parche es posible que ya se tenga acceso a los
equipos deseados.
*/
