/*
Es necesario tener siempre una contraseña fuerte, la cual es compuesta por letras (Mayus y minus), numeros y
caracteres especiales, de forma aleatoria.
Además es necesario tener multiples contraseñas para las multiples cuentas que se manejen, para poder
eficientar el almacenamiento de las contraseñas es recomendable el uso de alguna app certificada.
Tambien es altamente recomendable el uso de MFA, (Multi Factor Authentication), autorización de multifactores.
El MFA, es un sistema que requiere almenos dos elementos separados para poder permitir el acceso, por ejemplo:
-- Hardware Token: es un elemento con un display que muestra un número que va cambiando de forma periodica,
  número que se requiere ingresar junto con la contraseña.
-- Software Token: Funciona de igual forma que el hardware token, sin embargo puede ser que solo gener un
  token de acceso unico.

Adicional a todo es necesario que se realicen respaldos de forma periodica, adicionalmente tambien es necesirio
proteger estos respaldos con contraseña
*/
