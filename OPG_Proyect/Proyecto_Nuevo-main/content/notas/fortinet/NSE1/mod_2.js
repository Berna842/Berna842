/*
InfoSec, Seguridad de la información es importante para todos las personas que navegan en
internet.

La privacidad de la información se desgloza en los siguientes factores:
-- Coleción
-- Retención
-- Eliminación
*/

/*
La ciber seguridad comprende metodos de :
-- Protección de redes.
-- Protección de información
-- Protección de dispositivos

Para accesos no autorizados.

La seguridad de la información comprende dos ramos, la ciberseguridad y la seguridad física.
*/

/*
Las vulnerabilidades son todas los defectos del software, hadware o firmware que un atacante puede
explotar para poder tener acceso no autorizado a los sistemas.
La superficie de ataque, es todo lo que se encuentra expuesto en el entorno que un atacante puede utilizar
para poder sustraer o tener acceso a los lugares que se desean proteger.
El Malware es un archivo ó programa no deseado que puede dañar o comprometer la información almacenada en
la computadora. (Algunos son: Virus, Ransomware, DoS, etc)
Ingeniería social, su objetivo es ganar la confianza y una vez obtenida, explotar esa confianza para tener
acceso a la red.
*/

