/*Wifi*/

/*
Tecnología que permite conectar dispositivos en una red de area local de forma inalambrica, basado en la
norma estandar IEEE 802.11

Antiguamente el wifi era protegido por Privadicad Equivalente a Cableado (Wired Equivalent Privacy) WEP.
Este sistema utilizaba una llave para poder cifrar las comunicaciones, cifrado tipo RC4.

Debido a la vulnerabilidad de la WPE, se diseño el Acceso Inalambrico Protegido (Wi-Fi Protected Access) WPA
Sin embargo todavía no era completamente seguro ya que utilizaba el mismo cifrado por lo que se lanzo WPA2,
que estaba basado en la tecnología Advanced Encryption Standard (AES) creado por el Instituto Nacional de
Estandares y Tecnología (NIST).

Las empresas utilizaban los mecanismos de autenticación 802.1x similar a las redes cableadas.

Recientemente se lanzo la 3ra versión de Acceso Inalambrico Protegido WPA3, con una nueva tecnología de
encriptación 
*/
