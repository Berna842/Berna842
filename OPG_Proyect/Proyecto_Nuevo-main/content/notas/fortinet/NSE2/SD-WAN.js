/*Redes SD-WAN*/

/*
Una SD-WAN, es una red de area extensa definida por software, esta aprovecha las Wan corporativas y la
conectividad mutlinuve para poder ofrecer un rendimiento de alta velocidad.

Redes de Area Local, LAN(Local Area Network), una red WAN abarca una red geografíca extensa compuesta por
dos o más LAN.

Los SD-WAN integran balanceo de carga dependiendo del ancho de banda disponible, se creo en base a la
necesidad de integrar multiples dispositivos puntuales, además de que permite priorizar aplicaciones criticas
contando con una consola de administración unificada.

Otra de las ventajas es que permite:
  -- Un rendimiento óptimo de las aplicaciones.
  -- Mejor experiencia de usuario.
  -- Software como servicio.
  -- Comunicaciones unificadas como servicio (UCaaS)
*/
