/*Endpoint security*/

/*
Dispositivos terminales o Endpoints, anteriormente se le denominaba a cualquier dispositivo personal
utilizado por un usuario final, sin embargo actualmente tambien se incluyen los dispositivos IoT

Es importante proteger todos los dispositivos finales, incluyendo los IoT(Internet of things), ya
que estos tambien brindan acceso a la red y pueden llegar a permitir que se esparza malware atraves de
ellos.

Los primeros productos de seguridad diseñados para protegere los Endpoints fueron los antivirus (AV), esto
se encontraban basados en firmas, que son programas encargados de buscar malwares en los dispositivos.

Sin embargo existe el demoniminado Malware polimorfico, el cual esta diseñado para evolucionar por si solo
como lo hacen los virus en el mundo natural.

Debido a esta situación es que se desarollo la plataforma de protección de endpoints o EPP (Endpoint
Protection Platform), que integra diferentes teconologías para prevenir la ejecución de malware por archivos

El control de dispositivos, es una tecnologia integrada al igual que el control web.

Sin embargo aun continuaba evolucionando los ataques y las superficies de seguridad, por lo que se desarrollo
(EDR), Endpoint Detection and Response, incluyendo además Herramienta digital de investigación forense que
permite identificar los indicadores de compromiso (IoC) además permitiendo aislar endpoints.

Sin embago los EDR de primera generación tenian muchas limitantes en el dinamismo de los ataques, por lo que
se termino desarrollando la MDR (Managed Detection and Response), que permite notificar por medio de correos
de las posibles amenazas.

Los EDR de segunda generación funcionaban a base de políticas que permitían personalizar el control del entorno
y tambien permite definir el comportamiento ante el malware detectado, además de que permite revertir
los ataques de ransomware en tiempo real
*/
