/*
Gateway de correo electronico seguro.

SPAM: Mensajes Irrelevantes o no solicitados a través de internet y a una gran cantidad de destinatarios.
Phishing: Practica fraudulenta de envíar correos electronicos de una fuente que aparenta ser confiable.

Para poder protegerse del SPAM y Phishing, se implementaban filtros antispam, los cuales identificaban
palabras o patrones específico del encabecado o el cuerpo de los mensajes.

Sin embargo para poder complementar las seguridad los proveedores agregaron el Sender Policy Framework (SFP)
el cual es un metodo de autenticación de correo electronico que detecta remitentes y mensajes falsos

Los Gateways de correo electronico seguro surgieron para brindar mayor seguridad, integrando:
--> Scaneres de antivirus.
--> Simulación de Amenazas.
--> Entorno seguro de Sandbox.
*/
