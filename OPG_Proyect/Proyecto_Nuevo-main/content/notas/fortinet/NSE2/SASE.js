/* Secure Access Service Edge */

/*
SASE es una tecnología que combina las capacidades de la Red como servicio y de la seguridad como servicio,
entregandose por medio de la nube con el objetivo de ofrecer un acceso seguro a las redes empresariales
ya sean distribuidas o hibridas.

El objetivo de SASE es satisfacer las necesidades de acceso dinámico y seguro de las organizaciones actuales

Se elvan los costos cuando se trata de controlar todo el trafico que atravieza al centro de datos para ser
analizado, además de contar con mayor complejidad, mayor latencia y tener una gran exposición al riesgo.

Una solución basada en VPN, es poco practica, se genera mala experiencia a los usuarios cuando se integran
multiples nubes y brinda poca seguridad en los bordes.

Para poder dar solución a estos problemas es que se integra SASE, ya que permite tener integrado la seguridad
y el control de la red, la cual incluye lo siguiente:

Red aaS:
-- Intercambio de tráfico. Peering:
Que permite establecer conexiones de red e intercambiar tráfico directamente a través de internet, sin tener
que pagar a un tercer.

Seguridad aaS:
-- NGFW. Firewall de nueva generación o FWaaS:
Incluyendo capacidades de seguridad tales como
  --> Firewall como servicio basado en la nube.
  --> Capacidades de seguridad:
      * IPS
      * Anti-Malware
      * Inpección SSL.
      * Sandbox
-- Gateway seguro de web:
Protege a los usuario de amenazas en linea:
  --> Filtrado de malware
  --> Implementación de políticas de seguridad en Internet y de cumplimiento normativo.

-- ZTNA. Zero Trust Access Network:
  --> Ningún dispositivo ni usuario es confiable de manera automática.
  --> Todo intento de acceder a un sistema es verificado antes de que se conceda permiso.
Este tipo de conexión es integrado por varias tecnologías :
  --> MFA. Autenticación multifactor.
  --> Control de acceso seguro a la red NAC.
  --> Implementación de politicas de acceso.

-- Prevención de perdida de Datos. DLP:
  --> Impide que los usuarios finales muevan información hacia el exterior de la red.
  --> Estos sitemas informan de la inspección del contenido de las aplicaciónes de mensajería y correo
      electronico.

-- Sistema de nombres de dominio. DNS:
  --> Actua como una agenda telefónica para Internet.
  --> Proporciona un SASE con capacidades de detección de amenazas.
  --> Analiza y avalúa dominios peligrosos.

Los servicios SASE ofrecen:
  -->Rutas optimizadas para todos los usuarios y acceso a todas las nubes, lo que mejora el rendimiento y la
  agilidad.
  -->Seguridad certificada de nivel empresarial para la fuerza laboral móvil.
  -->Seguridad homogénea para todos los bordes.
  -->Administración consolidada de las operaciones de seguridad y de red.

NOTA: BYOD (Bring your own device) traiga su propio dispositivo, practica que permite a los usuarios
          conectarse a la red de la empresa con los dispositivos personales.
*/
