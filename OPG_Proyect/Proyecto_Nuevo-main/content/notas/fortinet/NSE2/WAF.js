/*Web Application Firwall */

/*
Firwalls de aplicaciones Web, es un dispositivo o Software que monitorea el tráfico HTTP y HTTPS y puede
bloquear el tráfico malicioso desde una aplicación web y hacía ella, la diferencia entre un WAF y un
Firewall convencional es que el objetivo del WAF es el contenido de aplicaciones web especificas y a nivel
de aplicación, en cambio los Firewall tradicionales crean puertas de enlace seguras entre los dispositivos
de red de area local y los servidores externos.

Los WAF pueden detener ataques que se originan en fallas de seguridad de las aplicaciones Web, como pueden
ser:
--> Inyecciones SQL.
--> Scripting entre sitios
--> Inclusión de archivos
--> Configuraciones incorrectas de seguridad.

Las primeras generaciones WAF utilizaban Listas negras y Atributos HTTP basados en Firmas, la siguiente
generación de WAF integrarón componentes de aprendizaje y tambien integro el metodo Heuristico y el monitoreo
de sesiones, sin embargo se tuvo que ingrear el Aprendizaje automatico, para poder realizar los analisis a
velocidad maquina y tambien se agregarón otras caracteristicas como:

--> Defensa contra los ataques DDoS
--> Reputación de IP
--> Antivirus
--> Prevención de perdida de datos DLP
*/
