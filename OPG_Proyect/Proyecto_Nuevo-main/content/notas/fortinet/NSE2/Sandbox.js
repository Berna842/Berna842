/*Sandbox*/

/*
Es un sistema que limita las acciones de una aplicación a un entorno virtual aislado, en este entorno
se analizan las interacciones de una aplicación en busca de posibles intentos maliciosos.

Este entorno permite combatir ataques de día cero.

Centros de Operaciones de Seguridad (SOC), debido a las constantes ataques basados en inteligencia artificial
se generarón estandares de análisis de amenazas, para estos estanadres MITRE propuso a ATT&CK

Las primeras generaciones de Sandbox tenian poca compatibilidad con otros dispositivos, la segunda generación
permitía eficientar la compartición de información de las amenazas de forma oportuna, y la tercera generación
permitió el analisis con la inteligencía artificial.
*/
