/*
La solución de Acesos de Cero confianza, (Zero-Trust Access [ZTA]) permite a las organizaciones poder tener el control y
visualizar a todo y a todos en la red, incluyendo si se encuentran o no en las instalaciones.
Permite además asegurar la consistencia del uso de las politicas de seguridad

Los problemas que soluciona el ZTA son:
--> Experiencias de usuario inconsistentes para mejorar la experiencia de usuario:
    --> Acceso identico para dentro y fuera de la oficina
    --> Tunel de acceso seguro para las aplicaciones.
    --> Single Sing-On (SSO) Soportado.
--> Proveen una visualizacion de aplicaciones ocultas de Malos actores, proveyendo mejor seguridad:
    --> No se necesita saber donde se encuentra ubicada la aplicación.

Principios de Fortinet ZTA:
--> Verifica:
    --> Verifica y autentica de forma continua, los usuarios y dispositivos.
--> Brinda accesos Minimos:
    --> Segmenta la red para crear zonas de control.
    --> Control de acceso para aplicaciones, información y recursos.
    --> Brinda privilegios de acceso minimos basados en necesidades o roles.
--> Asume que la red esta comprometida de forma permanente:
    --> Plan como si los atacantes estuvieran dentro y fuera de la red.
    --> Olvida el concepto de "Zona de confianza".

El acceso de cero confianza ayuda a mitigar los siguientes aspectos que han surgido debido al creciente numero de dispositivos en
el borde:

--> Autentifica la identidad de los usuarios por conección.
--> Soporta "Autenticación fuerte" - MFA y SSO.
--> Verifica la identidad de un dispositivo por sesión.
--> Verifica la "Postura del dispositivo" por sesión.
--> Restricción de acceso de usuarios para recursos basado en necesidad-de-saber.
--> Ocultando aplicaciones para el Internet.

El modelo de Cero confianza Forrester provee un estandar por cuales organizaciones pueden medir el progreso de exito de una solución
de Cero confianza.

*/
