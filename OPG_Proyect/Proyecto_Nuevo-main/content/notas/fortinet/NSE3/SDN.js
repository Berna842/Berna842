/*Security Driven Network Overview*/

/*
El security Driven Network proteje la información difundida atravez de los dispositivos de frontera,
computadoras, aplicaciones criticas y diferentes clientes , desde una misma aplicación.

La protección de amenazas tanto internas como externas las realiza con:
--> La segmentación interna.
--> Protección Automatizada
--> Reforzamiento de politicas.

La transformación digital es el proceso de usar diferentes tecnologías para crear un nuevo (o modificar un
existente), proceso organizacional, cultura, y experiencia de cliente para conocer el cambio de requerimientos

Los beneficios de la transformación digital es:
-- Mejora la collección de la información del usuario
-- Usa recursos de forma más eficiente.

Algunos de los elementos que integrán el SDN son:
--> FortiGate
--> FortiAP
--> FortiManager.
--> FortiSASE
--> FortiSwitch
--> FortiExtender

Sin embargo existen otros productos que contribuyen a la solución:
--> Acceso a la red Zero-trust (ZTNA): Refuerza la identidad del usuario y la postura del dispositivo.
--> Acceso Proxy: -- Intermediarios de conexión entre usuarios y aplicaciones.
                  -- Revisa la identidad de los usuarios, el comportamiento de los dispositivos y politicas
                  -- Monitorea la conexión
--> FortiManager: -- Simplifica la administración de usuarios, dispositivos, conexiones y politicas.
                  -- Administración de la seguridad desde un solo panel
                  -- Administración de red Automatizada
*/
