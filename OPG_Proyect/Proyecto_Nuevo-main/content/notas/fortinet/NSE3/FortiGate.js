/*
FortiGate es un firewall de siguiente generación (NGFW), este proteje los dispositivos de borde y los
dispositivos nucleo, además el resto de segmentos de red, este ejecuta FortiOS.
Tipicamente esta conectado al borde la red como su raiz, permitiendo administrar los dispositivos de red
como lo son:
  -- Access Points
  -- Endpoints
  -- Amenazas
  -- SaaS y software On-Premise

Algunas de las caracteristicas que integra el NGFW son:
  --> Control de aplicaciones.
  --> Prevensión de Intrución.
  --> Antivirus.
  --> Filtro de URL.
  --> Sandboxing.
  --> Inspección de SSL.

El SD-WAN es Software Define Wide Area Networks, la transición de SD-WAN a Secure SD-WAN reduce los costos
de implementación el secure SD-WAN abarca la siguientes tecnologías divididas en dos grandes grupos:
-- SD-WAN:
  --> Dirección de aplicaciones (Application Steering): Direccióna aplicaciones para realizar el mejor
                                              transporte de WAN.
  --> Corrección de rutas WAN (WAN path remediation): Optimiza el ancho de banda.
  --> VPN: Extiende una red privada atravez de una red publica.
-- NGFW:
  --> Control de aplicaciones (Application Control)
  --> Previsión de Intrusión (Intrusion prevention)
  --> Antivirus
  --> Filtrado de URL (URL filtering)
  --> Inspección de SSL (SSL Inspection)
  --> Sandboxing

FortiGate cuenta con los siguientes formas:
  -- Aparato fisico.
  -- VM (Maquinas virtuales[Private and Public Cloud])
  -- Firewall as a Service

FortiGate provee diferentes niveles de servicio para diferentes instituciones:
 -- FortiGate 30-80: Pequeñas empresas.
 -- FortiGate 100-900: Medianas empresas.
 -- FortiGate 1000-4000 y 6000: Branches o nucleos de grandes empresas.
 -- FortiGate 5000 y 7000: Centros de datos
 -- FortiGate 01 - Unlimited: Maquinas Virtuales, estos dependen del Hardware pero se pueden tener multiples
                        instancias.

Los dispositivos que controla FortiGate son:
  -- FortiManager: -- Manejo central de todos los dispositivos de seguridad de fabrica, tales como FortiGate
                  -- Provee refuerzo de politicas centralizadas y aprovisionamiento.
                  -- Simplifica la administración de red.

  -- FortiGuard: -- Analiticas de seguridad, IA (Inteligencia Artifical) / Malware, tan efectivo como diseñar y
                  entrenar.
*/
