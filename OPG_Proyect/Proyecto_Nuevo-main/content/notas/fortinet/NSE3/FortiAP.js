/*
FortiAP es un dispositivo de punto de acceso seguro, diseñado y producido por Fortine, que permite a los
usuarios acceder a la red  usando tecnologías Wi-Fi.
Algunos de los retos en los bordes de la LAN son:
  --> Número de Dispositivos.
  --> Seguridad.
  --> Administración.
El impacto de estos retos son:
  --> Incremento de presión y bajo rendimiento.
  --> Punto de soluciones de seguridad
  --> Incremente la dificultad de la administración y la escacez de personal capacitado

Se debe de extender la seguridad atraves de la capa de acceso, para esto Fortinet, provee los siguientes
elementos para poder tener una integración mejor y mejor consistencia de seguridad:
 -- FortiGate
 -- FortiAP
 -- FortiSwitch

Fortinet asegura los bordes de red LAN atravez de FortiLink, simplificando, bajando los costos e integrando
la seguridad, para esto, no requiere licencia, es flexible en escalabilidad, extiende las caracteristicas del
NGFW.

Las opciones de administración son:
-- Administración Local
-- Administración de nube via FortiCloud.
-- Administración de NOC/SOC con FortiManager, FortiAnalizer y FortiWLM.
-- Administración cloud para entornos no-FortiGate con nube FortiAP

FortiDeploy permite desplegar FortiAPs con solo unos clics, una vez se despliega el FortiAP desde el FortiDeploy,
FortiAP se conecta directamente a FortiAP Cloud para su administración por parte del cliente.
FortiDeploy permite configurar que FortiGate puede administrar a que FortiAP.

FortiExplorer tambien soporta FortiAPs administrados por FortiGate, es posiblementeproveer estadisticas e
información de la configuración.

FortiProcense, permite mostrar las analiticas del trafico visitante de un lugar.

*/
