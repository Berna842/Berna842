/*

FortiManager es un dispositivo tanto fisico como virtual que provee un solo panel de administración para
todos los dispositivos Fortinet, permitiendo tanto administrar como realizar deploys desde las herramientas
con las que cuenta.

Debido a la creciente complejidad de la administración de las redes, se ha creado Fabric Managment Center, el
cual es parte del nucleo de la plataforma Fortinet Security Fabric el cual permite simplificar la administración
y mantenimiento de la red.
Existe la posibilidad por parte de Fortiner de simplificar la administración y el aprovisionamiento de los
dispositivos de forma remota, siguiendo los pasos:

1) Se debe de registrar el dispositivo en el portal cloud.
2) Se debe de configurar el perfil en el portal.
3) Se debe de hacer push de las configuraciones.

Algunas de las capacidades clave son:
--> Plantillas SD-WAN y NGFW
--> Aprovisionamiento en menos de 6 minutos.
--> Una sola consola central de administración.
--> Respaldos de configuración con revisón de historicos.
--> DevOps playbooks y scripts

Algunas de las caracteristicas clave de FortiManager son:
--> Centralizar la administración para administrar configuraciones y politicas atravez de diferentes dispositivos.
--> Integración con Security Fabric, permite habilitar el "enfoque integrado", para asegurar una empresa y compartir
  la inteligencia de las amenazas con todos los elementos de la fabrica.
--> La administración del ciclo de vida de los dispositivos permite el aprovisionamiento en automatico y scripted, de
  actualizaciones de firewall, migraciones, y desaprovisionamiento.
--> control de roles de acceso para la red mediante el apalancamiento de su almacenamiento de identidad.

Analiticas de red:
Es un grafico en tiempo real que permite detectar amenazas avanzadas para la identificación de red y los riesgos.
Las capacidades clave son:
--> Visibilidad de la salud de la red.
--> Reporte a tiempo real del SLA.
--> Reporte Historico del SLA.
--> Reporte de uso de aplicaciones y paneles.
--> Manjeadores responsivos adaptados.

Reporte de cumplimiento:
Facilita el cumplimiento de las regulaciones.
caracteristicas clave:
--> Reportes de cumplimiento PCI.
--> Puntuación y clasificación de seguridad FSBP
--> Plantillas de regulaciones personalizables.
--> MFA para consolas de dispositivos
--> Integración con SIEM de terceros / Analiticas / Herramientas de cumplimiento

Automatización de red:
Flujos de trabajo integrados, reduce la complejidad de cambiar e integrar flujos de trabajos existentes.
caracteristicas clave:
--> Conectores para SDN y nube pública.
--> ITSM integración con ServiceNow.
--> Rest API vía fndn.fortinet.com
--> Herramientas DevOps como Ansible, Terraform, etc.

*/
